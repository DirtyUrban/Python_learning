import math

# Osnovne matematične operacije (Naloga 2 - Dan 1)
print(3 + 4)    # seštevanje
print(3 - 4)    # odštevanje
print(3 * 4)    # množenje
print(3 / 4)    # navadno deljenje
print(3 % 4)    # ostanek pri deljenju
print(3 ** 4)   # potenca
print(3 // 4)   # celoštevilsko deljenje

# Nizi (Naloga 3 - Dan 1)
