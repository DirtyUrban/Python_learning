#Numbers
#integer is a data type that represents whole numbers. In Python, integers can be positive or negative and can be of any length.
-3, -2, -1, 0, 1, 2, 3
#float is a data type that represents real numbers. In Python, floats can be positive or negative and can be of any length.
-3.14, -2.5, -1.0, 0.0, 1.0, 2.5, 3.14

#string is a data type that is used to represent text. In Python, strings are enclosed in either single quotes or double quotes.
'Urban'
'Slovenia'
'Python'
'I love learning Python'
'I am enjoying the first day of 30DaysOfPython Challenge'

#boolenan is a data type that can only have two values: True or False. It is used to represent the truth value of an expression.
True  # Is the light on? If it is on, then the value is True
False # Is the light on? If it is off, then the value is False

#list is a collection which is ordered and changeable. In Python lists are written with square brackets.
[0, 1, 2, 3, 4, 5]  # all are the same data types - a list of numbers
['Banana', 'Orange', 'Mango', 'Avocado'] # all the same data types - a list of strings (fruits)
['Finland','Estonia', 'Sweden','Norway'] # all the same data types - a list of strings (countries)
['Banana', 10, False, 9.81] # different data types in the list - string, integer, boolean and float

#dictionary is a collection of key-value pairs. Each key is separated from its value by a colon (:), the items are separated by commas, and the whole thing is enclosed in curly braces {}. An empty dictionary without any items is written with just two curly braces, like this: {}.
{
'first_name':'Asabeneh',
'last_name':'Yetayeh',
'country':'Finland',
'age':250,
'is_married':True,
'skills':['JS', 'React', 'Node', 'Python']
}

#tuple is a collection which is ordered and unchangeable. In Python tuples are written with round brackets.
(0, 1, 2, 3, 4, 5) # Numbers
('Asabeneh', 'Pawel', 'Brook', 'Abraham', 'Lidiya') # Names
('Earth', 'Jupiter', 'Neptune', 'Mars', 'Venus', 'Saturn', 'Uranus', 'Mercury') # planets

#set is a collection which is unordered and unindexed. In Python sets are written with curly brackets.
{2, 4, 3, 5}
{3.14, 9.81, 2.7} # order is not important in set
